    private void nameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameKeyPressed
        // TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_BACK_SPACE:
                break;
            case KeyEvent.VK_ENTER:
                name.setText(name.getText());
                break;
            default :
                EventQueue.invokeLater(new Runnable(){
                    @Override
                    
                public void run(){
                    String txt = name.getText();
                    autocomplete(txt);
                }    
                });
        }
    }//GEN-LAST:event_nameKeyPressed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        PreparedStatement st;
        ResultSet rs;
        
        try{
            String query = "select * from patient where name = ?";
            st = Project.MyConnection.getConnection().prepareStatement(query);
            st.setString(1, name.getText());
            rs = st.executeQuery();
            if(rs.next()){
                patientID.setText(rs.getString(1));
                name.setText(rs.getString(2));
                contactNumber.setText(rs.getString(3));
                age.setText(rs.getString(4));
                gender.setSelectedItem(rs.getString(5));
                bloodGroup.setText(rs.getString(6));
                address.setText(rs.getString(7));
                anyMajorDisease.setText(rs.getString(8));
                patientID.setEditable(false);      
            }
        }
        catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showInternalMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

