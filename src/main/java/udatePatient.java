    private void patient(){
        
        Connection con=Project.MyConnection.getConnection();
    try {
        Statement st = con.createStatement();
        String query = "select * from patient;";
        ResultSet rs = st.executeQuery(query);
        while(rs.next()){
                       
            String Nama = rs.getString("name");
            pname.add(Nama);
        }
        rs.close();
        st.close();
        con.close();
    } catch (SQLException ex) {
        Logger.getLogger(udatePatient.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }
    
    public void autocomplete(String txt){
        String complete = "";
        int start = txt.length();
        int last = txt.length();
        int a;
        for(a=0;a<pname.size();a++){
            if(pname.get(a).toString().startsWith(txt)){
                complete = pname.get(a).toString();
                last = complete.length();
                break;
            }
        }
        if(last>start){
            name.setText(complete);
            name.setCaretPosition(last);
            name.moveCaretPosition(start);
        }
    }
